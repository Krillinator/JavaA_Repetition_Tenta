package com.kristoffer.demo;

public class ifAndFor {

    public void ifAndForMethod() {
        // If & For-loop

        if (1 > 0) {
            System.out.println("TRUE");
        } else {
            System.out.println("FALSE");
        }

        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
    }

}
