package com.kristoffer.demo;

public class Arrays {

    public void MyArrays() {

        // Arrays
        int[] scores = { 150, 500, 1000 };

        for (int i = 0; i < scores.length; i++) {
            System.out.println(scores[i]);
        }

        // Index    -->     Position
        // Element  -->     Content
        // .length  -->     Length of..
        // scores   -->     Array

        // Arrays index... starts with 0..?
        // A - TRUE (YES)
        // B - FALSE

    }

}
